    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
//        processRequest(request, response);
        String username = request.getParameter("register-email");
        String password = request.getParameter("register-password");
        if(username!=null && password!=null){
            taikhoan tk = new taikhoan();
            tk.setUsername(username);
            tk.setPassword(password);
            tk.setAdmin(0);
            int result= ud.dangKyUser(tk);
            if(result!=0){
                RequestDispatcher rd = request.getRequestDispatcher("/registerSuccess");
                rd.forward(request, response);
            }
            else
            {
                RequestDispatcher rd = request.getRequestDispatcher("/registerFail");
                rd.forward(request, response);
            }
        }
    }

